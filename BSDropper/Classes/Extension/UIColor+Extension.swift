import UIKit

extension UIColor {
  @nonobjc class var bsDropperGray: UIColor {
    return UIColor(red: 187.0 / 255.0, green: 187.0 / 255.0, blue: 187.0 / 255.0, alpha: 1.0)
  }
  
  @nonobjc class var bsDropperBlack: UIColor {
    return UIColor(red: 47.0 / 255.0, green: 47.0 / 255.0, blue: 47.0 / 255.0, alpha: 1.0)
  }
  
  @nonobjc class var bsDropperNavy: UIColor {
    return UIColor(red: 27.0 / 255.0, green: 84.0 / 255.0, blue: 189.0 / 255.0, alpha: 1.0)
  }
  
  @nonobjc class var bsDropperDisable: UIColor {
    return UIColor(red: 192.0 / 255.0, green: 192.0 / 255.0, blue: 192.0 / 255.0, alpha: 1.0)
  }

  @nonobjc class var white245: UIColor {
    return UIColor(red: 245/255, green: 245/255, blue: 245/255, alpha: 1.0)
  }
  
}
